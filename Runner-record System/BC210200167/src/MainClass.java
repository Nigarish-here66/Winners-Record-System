    private void deleteAllBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteAllBtnActionPerformed
            int confirm = JOptionPane.showConfirmDialog(this, "Do You Want To Remove All Inventors?");
    if (confirm == JOptionPane.YES_OPTION) {
        try {
            // Call the delete method to remove all inventors
            DbHelper.deleteInventor();
            
            // Optionally, show a confirmation message
            JOptionPane.showMessageDialog(this, "All Inventors have been removed.");
            
            // Reload the data after deletion to update the UI
            loadInventorsData();
        } catch (SQLException ex) {
            // Log the error and show a message to the user if the operation fails
            Logger.getLogger(MainClass.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, "Error deleting inventors: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
    }//GEN-LAST:event_deleteAllBtnActionPerformed

    private void viewWinnerBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewWinnerBtnActionPerformed
           List<Inventor> run = null;
        try {
            run = DbHelper.getTopInventor();
        } catch (SQLException ex) {
            Logger.getLogger(MainClass.class.getName()).log(Level.SEVERE, null, ex);
        }
                tableModel.setRowCount(0);
                for (int i = 0; i < run.size(); i++) {
                    Inventor r = run.get(i);
                    Object[] rowData = {i + 1, r.getName(), r.getScore()};
                    tableModel.addRow(rowData);
                }
    }//GEN-LAST:event_viewWinnerBtnActionPerformed

    private void loadInventorsData() throws SQLException {
            List<Inventor> run = DbHelper.getTopInventor();
            tableModel.setRowCount(0);
            for (int i = 0; i < run.size(); i++) {
                Inventor r = run.get(i);
                Object[] rowData = {i + 1, r.getName(), r.getScore()};
                tableModel.addRow(rowData);
            }    }
            
