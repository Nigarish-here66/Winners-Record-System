    private void addInventorBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addInventorBtnActionPerformed
   String name = nameText.getText();
        String scoreStr = scoreText.getText();
        if (name.isEmpty() || scoreStr.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Name and score cannot be empty.");
            return;
        }

        try {
            int score = Integer.parseInt(scoreStr);
            if (score < 1 || score > 100) {
                JOptionPane.showMessageDialog(this, "Score must be between 1 and 100.");
                return;
            }
            Inventor inventor = new Inventor(0, name, score);
            if (DbHelper.insertInventor(inventor)) {
                JOptionPane.showMessageDialog(this, "Inventor added successfully.");
                nameText.setText("");
                scoreText.setText("");
            } else {
                JOptionPane.showMessageDialog(this, "Inventor with this name already exists.");
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Score must be a number between 1 and 100.");
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "An error occurred while adding the inventor.");
            e.printStackTrace();
        }
    }//GEN-LAST:event_addInventorBtnActionPerformed

